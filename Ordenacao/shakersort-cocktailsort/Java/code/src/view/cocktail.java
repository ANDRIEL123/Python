    private void cocktail(int[] lista) {
        boolean houveTroca;
        
        do{
            houveTroca = false;
            for (int i = 0; i < lista.length - 1; i++) {
                if(lista[i] > lista[i + 1]) {
                   int temp = lista[i];
                   lista[i] = lista[i + 1];
                   lista[i + 1] = temp;
                   houveTroca = true;
                }
            }
            
            for(int i = lista.length - 1; i > 0; i--) {
                if(lista[i] < lista[i - 1]) {
                    int temp = lista[i];
                    lista[i] = lista[i - 1];
                    lista[i - 1] = temp;
                    houveTroca = true;
                }
            } 
            
        } while(houveTroca);
    }
    
    private void insereNaLista(int[] lista) {
        DefaultListModel model = new DefaultListModel();
        
        for(int i = 0; i < lista.length; i++) {
            
           model.addElement(lista[i]);
           jList1.setModel(model);
        }
        
        
    }
    
